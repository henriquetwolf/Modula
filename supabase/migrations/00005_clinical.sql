-- EVALUATIONS (unified base table — type discriminator + JSONB metadata)
CREATE TABLE public.evaluations (
    id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id uuid NOT NULL REFERENCES public.tenants(id) ON DELETE CASCADE,
    unit_id uuid NOT NULL REFERENCES public.units(id),
    client_id uuid NOT NULL REFERENCES public.client_profiles(id),
    professional_id uuid NOT NULL REFERENCES public.user_profiles(id),
    type evaluation_type NOT NULL,
    status evaluation_status NOT NULL DEFAULT 'draft',
    title varchar(200),
    scheduled_at timestamptz,
    started_at timestamptz,
    completed_at timestamptz,
    notes text,

    -- Type-specific data (physical: bodyComp, skinFolds; physio: painMap, ROM; etc.)
    metadata jsonb NOT NULL DEFAULT '{}',

    -- Summary generated by professional or AI
    summary text,
    ai_insights jsonb DEFAULT '{}',

    version integer NOT NULL DEFAULT 1,
    previous_version_id uuid REFERENCES public.evaluations(id),
    created_at timestamptz NOT NULL DEFAULT now(),
    updated_at timestamptz NOT NULL DEFAULT now(),
    created_by uuid NOT NULL REFERENCES public.user_profiles(id)
);

CREATE TRIGGER evaluations_updated_at
    BEFORE UPDATE ON public.evaluations
    FOR EACH ROW EXECUTE FUNCTION handle_updated_at();

CREATE INDEX idx_evaluations_tenant ON public.evaluations(tenant_id);
CREATE INDEX idx_evaluations_client ON public.evaluations(tenant_id, client_id);
CREATE INDEX idx_evaluations_prof ON public.evaluations(tenant_id, professional_id);
CREATE INDEX idx_evaluations_date ON public.evaluations(tenant_id, completed_at DESC);
CREATE INDEX idx_evaluations_physical ON public.evaluations(client_id, completed_at) WHERE type = 'physical';
CREATE INDEX idx_evaluations_physio ON public.evaluations(client_id, completed_at) WHERE type = 'physio';
CREATE INDEX idx_evaluations_nutrition ON public.evaluations(client_id, completed_at) WHERE type = 'nutrition';
CREATE INDEX idx_evaluations_metadata ON public.evaluations USING GIN(metadata);


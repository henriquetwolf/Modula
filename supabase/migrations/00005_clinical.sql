-- PROGRESS NOTES (session notes, evolution records — SOAP format for physio)
CREATE TABLE public.progress_notes (
    id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id uuid NOT NULL REFERENCES public.tenants(id) ON DELETE CASCADE,
    unit_id uuid NOT NULL REFERENCES public.units(id),
    client_id uuid NOT NULL REFERENCES public.client_profiles(id),
    professional_id uuid NOT NULL REFERENCES public.user_profiles(id),
    plan_id uuid REFERENCES public.plans(id),
    evaluation_id uuid REFERENCES public.evaluations(id),
    type varchar(50) NOT NULL DEFAULT 'general',  -- general, soap, fitness, nutrition
    title varchar(200),

    -- SOAP fields (especially for physio)
    subjective text,
    objective text,
    assessment text,
    plan_notes text,       -- "plan" in SOAP
    general_notes text,    -- free-form notes for non-SOAP

    metadata jsonb NOT NULL DEFAULT '{}',

    session_date timestamptz NOT NULL DEFAULT now(),
    version integer NOT NULL DEFAULT 1,
    created_at timestamptz NOT NULL DEFAULT now(),
    updated_at timestamptz NOT NULL DEFAULT now(),
    created_by uuid NOT NULL REFERENCES public.user_profiles(id)
);

CREATE TRIGGER progress_notes_updated_at
    BEFORE UPDATE ON public.progress_notes
    FOR EACH ROW EXECUTE FUNCTION handle_updated_at();

CREATE INDEX idx_progress_notes_tenant ON public.progress_notes(tenant_id);
CREATE INDEX idx_progress_notes_client ON public.progress_notes(tenant_id, client_id);

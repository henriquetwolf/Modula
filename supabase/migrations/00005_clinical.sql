-- DOCUMENTS (file attachments — exams, photos, contracts, reports)
CREATE TABLE public.documents (
    id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id uuid NOT NULL REFERENCES public.tenants(id) ON DELETE CASCADE,
    unit_id uuid REFERENCES public.units(id),
    client_id uuid REFERENCES public.client_profiles(id),
    uploaded_by uuid NOT NULL REFERENCES public.user_profiles(id),
    category document_category NOT NULL DEFAULT 'other',
    title varchar(200) NOT NULL,
    description text,
    file_name varchar(255) NOT NULL,
    file_path text NOT NULL,                      -- Supabase Storage path
    file_size bigint,
    mime_type varchar(100),
    sensitivity_level sensitive_level NOT NULL DEFAULT 'low',

    -- Optional links to entities
    evaluation_id uuid REFERENCES public.evaluations(id),
    plan_id uuid REFERENCES public.plans(id),

    tags text[] DEFAULT '{}',
    metadata jsonb NOT NULL DEFAULT '{}',
    is_archived boolean NOT NULL DEFAULT false,
    created_at timestamptz NOT NULL DEFAULT now(),
    updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TRIGGER documents_updated_at
    BEFORE UPDATE ON public.documents
    FOR EACH ROW EXECUTE FUNCTION handle_updated_at();

CREATE INDEX idx_documents_tenant ON public.documents(tenant_id);
CREATE INDEX idx_documents_client ON public.documents(tenant_id, client_id);
CREATE INDEX idx_documents_category ON public.documents(tenant_id, category);

